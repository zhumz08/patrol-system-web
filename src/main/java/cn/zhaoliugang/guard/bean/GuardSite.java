package cn.zhaoliugang.guard.bean;

public class GuardSite {
	private int id;
	private String guardsiteid;
	private String guardsitename;
	private String cameraid;
	private String cameraname;
	private String presetid;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getGuardsiteid() {
		return guardsiteid;
	}
	public void setGuardsiteid(String guardsiteid) {
		this.guardsiteid = guardsiteid;
	}
	public String getGuardsitename() {
		return guardsitename;
	}
	public void setGuardsitename(String guardsitename) {
		this.guardsitename = guardsitename;
	}
	public String getCameraid() {
		return cameraid;
	}
	public void setCameraid(String cameraid) {
		this.cameraid = cameraid;
	}
	public String getCameraname() {
		return cameraname;
	}
	public void setCameraname(String cameraname) {
		this.cameraname = cameraname;
	}
	public String getPresetid() {
		return presetid;
	}
	public void setPresetid(String presetid) {
		this.presetid = presetid;
	}
	
}
